class AIThreatAlert(models.Model):
    """
    Django ORM model representing AI Threat Predictions / Alerts (`prediction_logs`).
    Set managed = False so Django never alters the table managed by FastAPI / SQLAlchemy.
    Stores real-time predictions generated by CNN-LSTM (Label 1: Reconnaissance, Label 2: Lateral Movement, Label 3: Beaconing).
    """
    id = models.AutoField(primary_key=True)
    created_at = models.DateTimeField(null=True, blank=True, db_index=True)
    ip_origin = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    label = models.IntegerField(db_index=True)  # 0: Normal, 1: Reconnaissance, 2: Lateral Movement, 3: Beaconing
    threat_name = models.CharField(max_length=100, null=True, blank=True, db_index=True)
    confidence_score = models.FloatField()
    sequence_metadata = SafeJSONField(default=dict, null=True, blank=True)

    class Meta:
        managed = False
        db_table = "prediction_logs"
        ordering = ["-created_at"]
        verbose_name = "AI Threat Alert"
        verbose_name_plural = "AI Threat Alerts"

    @property
    def risk_level(self):
        if self.label == 3:
            return "Critical"
        elif self.label in (1, 2):
            return "High"
        return "Normal"

    @property
    def risk_badge_class(self):
        if self.label == 3:
            return "bg-red-100 text-red-700 border border-red-300 animate-pulse"
        elif self.label in (1, 2):
            return "bg-orange-100 text-orange-700 border border-orange-300"
        return "bg-slate-100 text-slate-600 border border-slate-200"


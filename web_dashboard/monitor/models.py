from django.db import models


class SafeJSONField(models.JSONField):
    """
    Custom JSONField to safely handle PostgreSQL JSONB values that psycopg driver
    has already deserialized into Python dict/list/etc.
    """
    def from_db_value(self, value, expression, connection):
        if value is None:
            return value
        if isinstance(value, (dict, list, int, float, bool)):
            return value
        try:
            return super().from_db_value(value, expression, connection)
        except TypeError:
            return value



class BaseFirewallLog(models.Model):
    """
    Abstract Django ORM Model for Cybersecurity Firewall Logs.
    Maps to existing PostgreSQL tables created and owned by FastAPI / SQLAlchemy.
    """
    id = models.AutoField(primary_key=True)
    log_date = models.DateTimeField(null=True, blank=True, db_index=True)
    ip_origin = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    ip_impacted = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    port_impacted = models.IntegerField(null=True, blank=True, db_index=True)
    zone_origin = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    zone_impacted = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    log_source = models.CharField(max_length=255, null=True, blank=True, db_index=True)
    port_origin = models.IntegerField(null=True, blank=True, db_index=True)
    protocol = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    action = models.CharField(max_length=100, null=True, blank=True, db_index=True)
    severity = models.CharField(max_length=50, null=True, blank=True, db_index=True)
    threat_name = models.CharField(max_length=255, null=True, blank=True, db_index=True)
    application = models.CharField(max_length=100, null=True, blank=True, db_index=True)
    country_origin = models.CharField(max_length=100, null=True, blank=True)
    country_impacted = models.CharField(max_length=100, null=True, blank=True)
    rule_name = models.CharField(max_length=255, null=True, blank=True)
    classification = models.CharField(max_length=255, null=True, blank=True, db_index=True)
    additional_data = SafeJSONField(default=dict, null=True, blank=True)

    class Meta:
        abstract = True



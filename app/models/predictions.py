"""
AI Prediction & Threat Alert ORM Model (`Section 4.4.2.5` & `Poin 11-12 Bab 2`)
===================================================================================
Stores real-time predictions generated by the hybrid CNN-LSTM neural network
when evaluating incremental 5-minute behavior windows (`timesteps=3`) per IP address.

Used by:
- Pipeline Worker & FastAPI Engine: To permanently store AI detection results post-storage (`Poin 11`).
- Django SOC Dashboard: To retrieve active Long Running Attack threats (`Reconnaissance`, `Lateral Movement`, `Beaconing`)
  for the SOC Alert panels and AI Chatbot (`Poin 12 & 13`).
"""

from sqlalchemy import Column, Integer, String, Float, DateTime, JSON
from sqlalchemy.sql import func
from app.core.database import Base


class PredictionLog(Base):
    """
    SQLAlchemy ORM Model representing an AI Threat Prediction / Alert.
    Mapped to PostgreSQL table `prediction_logs`.
    """
    __tablename__ = "prediction_logs"

    id = Column(Integer, primary_key=True, index=True, autoincrement=True)
    created_at = Column(DateTime(timezone=True), server_default=func.now(), index=True)
    
    # Target Evaluation Metadata
    ip_origin = Column(String(50), index=True, nullable=False)
    
    # AI CNN-LSTM Inference Output
    label = Column(Integer, nullable=False, index=True)  # 0: Normal, 1: Reconnaissance, 2: Lateral Movement, 3: Beaconing
    threat_name = Column(String(100), nullable=False, index=True)
    confidence_score = Column(Float, nullable=False)
    
    # Additional context or sequence summary (e.g. unique ports scanned, connection count during window)
    sequence_metadata = Column(JSON, default=dict)

    def __repr__(self):
        return f"<PredictionLog(ip={self.ip_origin}, label={self.label}, threat={self.threat_name}, conf={self.confidence_score:.2f})>"
